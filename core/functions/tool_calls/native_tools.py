# 可选：中文分词
def zh_preprocess(text: str):
    try:
        import re, jieba
        text = (text or "").strip()
        text = re.sub(r"\s+", " ", text)
        return list(jieba.cut(text, cut_all=False))
    except Exception:
        return (text or "").split()

def _meta_clean(m: Dict[str, Any]) -> Dict[str, Any]:
    # 只剔除 None 和 ""；保留 0 / False
    return {k: v for k, v in (m or {}).items() if v is not None and v != ""}

def _format_hits_mandatory(docs: List[Document]) -> str:
    lines: List[str] = []
    for i, d in enumerate(docs, 1):
        if lines:
            lines.append("")
            lines.append("---")
            lines.append("")
        lines.append(f"[{i}]")
        lines.append((d.page_content or "").strip())
        md = _meta_clean(d.metadata or {})
        if md:
            lines.append("metadata:")
            for k, v in md.items():
                lines.append(f"- {k}: {v}")
    return "\n".join(lines)


def _expand_bm25_query(query: str) -> str:
    raw = str(query or "").strip()
    if not raw:
        return raw

    extras: List[str] = []

    def _add(*terms: str) -> None:
        for term in terms:
            term = str(term or "").strip()
            if term and term not in raw and term not in extras:
                extras.append(term)

    if any(token in raw for token in ["一平", "每平", "平米", "每平方米", "平方米"]):
        _add("平米", "每平米", "元/平米", "万元/平米")
    if any(token in raw for token in ["房价", "房屋价格", "房屋售价"]):
        _add("房屋", "售卖广告", "原价", "现价")
    if any(token in raw for token in ["多少钱", "价格", "售价", "价钱"]):
        _add("多少元", "现价", "元")

    if not extras:
        return raw
    return raw + " " + " ".join(extras)


class _DocumentMappingIndex:
    """In-memory resolver for title/subtitle <-> document_id mapping."""

    def __init__(self, *, index_path: str, doc2chunks_path: Optional[str] = None, doc_type: str = "general"):
        self.index_path = index_path
        self.doc2chunks_path = doc2chunks_path
        self.doc_type = str(doc_type or "general").strip().lower() or "general"
        meta = DOC_TYPE_META.get(self.doc_type, DOC_TYPE_META.get("general", {}))
        self.title_field = str(meta.get("title", "title")).strip() or "title"
        self.subtitle_field = str(meta.get("subtitle", "subtitle")).strip() or "subtitle"

        # title_key = "<title> / <subtitle>" (subtitle 为空时退化为 title)
        self.title_key2document_ids: Dict[str, List[str]] = {}
        self.document_id2title_key: Dict[str, str] = {}
        self.document_id2title: Dict[str, str] = {}
        self.document_id2subtitle: Dict[str, str] = {}
        self._load()

    def _load_json(self, path: str, default: Any) -> Any:
        if not path or not os.path.exists(path):
            return default
        try:
            with open(path, "r", encoding="utf-8") as f:
                return json.load(f)
        except Exception:
            return default

    @staticmethod
    def _uniq_str_list(items: Any) -> List[str]:
        if not isinstance(items, list):
            return []
        out: List[str] = []
        seen = set()
        for x in items:
            s = str(x).strip()
            if not s or s in seen:
                continue
            seen.add(s)
            out.append(s)
        return out

    @staticmethod
    def _compose_title_key(title: str, subtitle: str) -> str:
        t = str(title or "").strip()
        st = str(subtitle or "").strip()
        if t and st:
            return f"{t} / {st}"
        return t or st

    @staticmethod
    def _norm(s: str) -> str:
        return " ".join(str(s or "").strip().lower().split())

    def _load(self) -> None:
        # 优先使用 doc2chunks（含 doc_type 对应 title/subtitle 字段）
        doc2chunks = self._load_json(self.doc2chunks_path or "", {})
        if isinstance(doc2chunks, dict) and doc2chunks:
            for source_doc, pack in doc2chunks.items():
                source_doc = str(source_doc).strip()
                if not source_doc:
                    continue

                md = (pack or {}).get("document_metadata") or {}
                if not isinstance(md, dict):
                    md = {}

                # Prefer doc-type-specific fields, but keep robust fallbacks for
                # older/newer metadata layouts generated by graph_builder.
                title = str(
                    md.get(self.title_field)
                    or md.get("title")
                    or md.get("doc_title")
                    or md.get("source_title")
                    or ""
                ).strip()
                subtitle = str(
                    md.get(self.subtitle_field)
                    or md.get("subtitle")
                    or md.get("sub_title")
                    or md.get("sub_scene_name")
                    or ""
                ).strip()
                title_key = self._compose_title_key(title, subtitle)

                if title_key:
                    self.title_key2document_ids.setdefault(title_key, [])
                    self.title_key2document_ids[title_key].append(source_doc)
                    self.document_id2title_key[source_doc] = title_key
                else:
                    # Keep exact reverse lookup available even if title fields are missing.
                    self.document_id2title_key[source_doc] = source_doc

                self.document_id2title[source_doc] = title
                self.document_id2subtitle[source_doc] = subtitle

            for k, ids in list(self.title_key2document_ids.items()):
                self.title_key2document_ids[k] = self._uniq_str_list(ids)
            return

        # 兜底：仅有 doc2chunks_index.json 时，至少保留 title_key <-> source_documents
        idx = self._load_json(self.index_path, {})
        if not isinstance(idx, dict):
            return
        t2k = idx.get("title2keys") or {}
        k2t = idx.get("key2title") or {}
        if isinstance(t2k, dict):
            for t, ids in t2k.items():
                title_key = str(t).strip()
                if not title_key:
                    continue
                clean_ids = self._uniq_str_list(ids)
                self.title_key2document_ids[title_key] = clean_ids
                for sid in clean_ids:
                    self.document_id2title_key[sid] = title_key
        if isinstance(k2t, dict):
            for sid, tk in k2t.items():
                s = str(sid).strip()
                title_key = str(tk).strip()
                if not s or not title_key:
                    continue
                self.document_id2title_key[s] = title_key
                self.document_id2title[s] = title_key
                self.document_id2subtitle[s] = ""

    def match_title_records(
        self,
        *,
        title: str = "",
        subtitle: str = "",
        fuzzy: bool = True,
        limit: int = 20,
    ) -> List[Dict[str, Any]]:
        title = str(title or "").strip()
        subtitle = str(subtitle or "").strip()
        if not title and not subtitle:
            return []

        records: Dict[str, Dict[str, Any]] = {}
        for sid, title_key in self.document_id2title_key.items():
            key = str(title_key or "").strip()
            if not key:
                continue
            rec = records.setdefault(
                key,
                {
                    "title_key": key,
                    "title": str(self.document_id2title.get(sid, "") or "").strip(),
                    "subtitle": str(self.document_id2subtitle.get(sid, "") or "").strip(),
                    "document_ids": [],
                },
            )
            rec["document_ids"].append(str(sid))

        def _field_score(query_text: str, target_text: str, *, threshold: float) -> float:
            q = self._norm(query_text)
            t = self._norm(target_text)
            if not q:
                return 0.0
            if not t:
                return -1.0
            if q == t:
                return 1.5
            if q in t or t in q:
                return 1.0
            if not fuzzy:
                return -1.0
            ratio = difflib.SequenceMatcher(None, q, t).ratio()
            return ratio if ratio >= threshold else -1.0

        scored: List[tuple[float, Dict[str, Any]]] = []
        for rec in records.values():
            title_score = _field_score(title, rec.get("title", ""), threshold=0.55) if title else 0.0
            subtitle_score = _field_score(subtitle, rec.get("subtitle", ""), threshold=0.5) if subtitle else 0.0

            if title and title_score < 0:
                continue
            if subtitle and subtitle_score < 0:
                continue

            score = 0.0
            if title:
                score += 0.7 * title_score
            if subtitle:
                score += 0.3 * subtitle_score
                if title and subtitle and subtitle_score > 0 and title_score > 0:
                    score += 0.1
            scored.append((score, rec))

        scored.sort(
            key=lambda x: (
                x[0],
                len(x[1].get("document_ids", [])),
                x[1].get("title_key", ""),
            ),
            reverse=True,
        )
        out: List[Dict[str, Any]] = []
        for _, rec in scored[: max(1, int(limit))]:
            clean = dict(rec)
            clean["document_ids"] = self._uniq_str_list(clean.get("document_ids", []))
            out.append(clean)
        return out

    def match_title_keys(self, query: str, *, fuzzy: bool = True, limit: int = 20) -> List[str]:
        q = str(query or "").strip()
        if not q:
            return []
        if q in self.title_key2document_ids:
            return [q]
        if not fuzzy:
            return []

        qn = self._norm(q)
        scored: List[tuple] = []
        for key in self.title_key2document_ids.keys():
            kn = self._norm(key)
            score = 0.0
            if qn in kn or kn in qn:
                score = 1.0
            else:
                ratio = difflib.SequenceMatcher(None, qn, kn).ratio()
                if ratio >= 0.55:
                    score = ratio
            if score > 0:
                scored.append((score, key))
        scored.sort(key=lambda x: x[0], reverse=True)
        return [k for _, k in scored[: max(1, int(limit))]]

    def match_document_ids(self, query: str, *, fuzzy: bool = True, limit: int = 20) -> List[str]:
        q = str(query or "").strip()
        if not q:
            return []
        if q in self.document_id2title_key:
            return [q]
        if not fuzzy:
            return []

        qn = self._norm(q)
        scored: List[tuple] = []
        for sid in self.document_id2title_key.keys():
            sn = self._norm(sid)
            score = 0.0
            if qn in sn or sn in qn:
                score = 1.0
            else:
                ratio = difflib.SequenceMatcher(None, qn, sn).ratio()
                if ratio >= 0.6:
                    score = ratio
            if score > 0:
                scored.append((score, sid))
        scored.sort(key=lambda x: x[0], reverse=True)
        return [sid for _, sid in scored[: max(1, int(limit))]]


