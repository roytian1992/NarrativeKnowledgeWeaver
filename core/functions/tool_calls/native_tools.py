@register_tool("lookup_titles_by_document_ids")
class LookupTitlesByDocumentIDsTool(_BaseDocumentMappingTool):
    name = "lookup_titles_by_document_ids"
    description = (
        "把一个或多个 document_id 批量映射成可读的场次/章节标题。"
        "适合在拿到实体的 source_documents 或检索命中的 document_id 后，整理出场次列表。"
    )
    parameters = [
        {"name": "document_ids", "type": "array", "description": "待查询的 document_id 列表。", "required": True},
    ]

    def call(self, params: str, **kwargs) -> str:
        logger.info("🔎 调用 lookup_titles_by_document_ids")
        try:
            p: Dict[str, Any] = json.loads(params or "{}")
        except Exception as e:
            return f"参数解析失败：{e}"

        document_ids = self._dedup_str_list(p.get("document_ids"))
        if not document_ids:
            return "document_ids 不能为空"

        lines: List[str] = []
        hit = False
        for sid in document_ids:
            title = self.index.document_id2title.get(sid, "")
            subtitle = self.index.document_id2subtitle.get(sid, "")
            if not any([title, subtitle]):
                lines.append(f"document_id: {sid}")
                lines.append("title: not_found")
                lines.append("")
                continue
            hit = True
            lines.append(f"document_id: {sid}")
            lines.append(f"title: {title}")
            if subtitle:
                lines.append(f"subtitle: {subtitle}")
            lines.append("")

        if not hit:
            return "未找到任何 document_id 对应的 title/subtitle。"
        return "\n".join(lines).strip()


@register_tool("lookup_document_ids_by_title")
class LookupDocumentIDsByTitleTool(_BaseDocumentMappingTool):
    name = "lookup_document_ids_by_title"
    description = (
        "根据标题和副标题查询对应的 document_id。"
        "title 和 subtitle 均可选，且都支持模糊匹配，适合由场次/章节名称反查 source_documents。"
    )
    parameters = [
        {"name": "title", "type": "string", "description": "标题，可为空。", "required": False},
        {"name": "subtitle", "type": "string", "description": "副标题，可为空。", "required": False},
        {"name": "fuzzy", "type": "boolean", "description": "是否启用模糊匹配，默认 true。", "required": False},
        {"name": "limit", "type": "integer", "description": "最多返回多少个匹配到的文本块 document_id，默认 10。", "required": False},
    ]

    def call(self, params: str, **kwargs) -> str:
        logger.info("🔎 调用 lookup_document_ids_by_title")
        try:
            p: Dict[str, Any] = json.loads(params or "{}")
        except Exception as e:
            return f"参数解析失败：{e}"

        title = str(p.get("title", "")).strip()
        subtitle = str(p.get("subtitle", "")).strip()
        fuzzy = bool(p.get("fuzzy", True))
        limit = max(1, int(p.get("limit", 10) or 10))
        if not title and not subtitle:
            return "title 和 subtitle 不能同时为空"

        matched_records = self.index.match_title_records(
            title=title,
            subtitle=subtitle,
            fuzzy=fuzzy,
            limit=limit,
        )
        if not matched_records:
            query_text = title if title and not subtitle else f"{title} / {subtitle}".strip(" /")
            return f"未找到 title/subtitle 对应 document_id：{query_text}"

        lines: List[str] = []
        returned = 0
        for rec in matched_records:
            if rec.get("title"):
                lines.append(f"title: {rec['title']}")
            if rec.get("subtitle"):
                lines.append(f"subtitle: {rec['subtitle']}")
            lines.append("document_ids:")
            ids = self.index._uniq_str_list(rec.get("document_ids", []))
            remain = max(0, limit - returned)
            if remain <= 0:
                break
            for sid in ids[:remain]:
                lines.append(f"- {sid}")
                returned += 1
            lines.append("")
            if returned >= limit:
                break
        return "\n".join(lines).strip()


@register_tool("search_related_content")
class SearchRelatedContentTool(BaseTool):
    """
    Retrieve documents by document_id and extract query-related content in parallel.
    """

    name = "search_related_content"
    description = (
        "输入一个或多个 document_id 和 query，"
        "从 document-level 向量库读取原文后并发抽取与 query 直接相关的内容。"
        "仅返回提取结果非空的 document_id。"
    )
    parameters = [
        {
            "name": "document_ids",
            "type": "array",
            "description": "document_id 列表；兼容单个字符串。",
            "required": True,
        },
        {
            "name": "query",
            "type": "string",
            "description": "要抽取的目标问题/查询。",
            "required": True,
        },
        {
            "name": "max_length",
            "type": "integer",
            "description": "每个 document 的提取内容最大长度，默认 300。",
            "required": False,
        },
    ]

    def __init__(self, document_vector_store: Any, document_parser: Any, *, max_workers: int = 8):
        self.doc_vs = document_vector_store
        self.document_parser = document_parser
        self.max_workers = max(1, int(max_workers))

    @staticmethod
    def _dedup_ids(raw: Any) -> List[str]:
        if isinstance(raw, str):
            raw = [raw]
        if not isinstance(raw, list):
            return []
        out: List[str] = []
        seen = set()
        for x in raw:
            s = str(x).strip()
            if not s or s in seen:
                continue
            seen.add(s)
            out.append(s)
        return out

    @staticmethod
    def _parse_related_content(raw: str) -> str:
        try:
            data = json.loads(correct_json_format(raw))
            val = data.get("related_content", "")
            return val.strip() if isinstance(val, str) else ""
        except Exception:
            return ""

    def call(self, params: str, **kwargs) -> str:
        if str(os.environ.get("NKW_DISABLE_SEARCH_RELATED_CONTENT", "") or "").strip().lower() in {
            "1",
            "true",
            "yes",
            "on",
        }:
            return "（无）"
        logger.info("🔎 调用 search_related_content")
        try:
            p: Dict[str, Any] = json.loads(params or "{}")
        except Exception as e:
            return f"参数解析失败：{e}"

        document_ids = self._dedup_ids(p.get("document_ids"))
        query = str(p.get("query", "")).strip()
        max_length = p.get("max_length", 300)
        try:
            max_length = int(max_length)
            if max_length <= 0:
                max_length = 300
        except Exception:
            max_length = 300

        if not document_ids:
            return "document_ids 不能为空"
        if not query:
            return "query 不能为空"

        docs = self.doc_vs.search_by_document_ids(document_ids)
        id2parts: Dict[str, List[str]] = {}
        for d in docs or []:
            metadata = getattr(d, "metadata", {}) or {}
            did = str(metadata.get("document_id") or getattr(d, "id", "") or "").strip()
            txt = str(getattr(d, "content", "") or "").strip()
            if did and txt:
                id2parts.setdefault(did, []).append(txt)

        id2text = {
            did: "\n".join(parts).strip()
            for did, parts in id2parts.items()
            if parts
        }

        if not id2text:
            return "（无）"

        def _work(item: tuple[str, str]) -> tuple[str, str]:
            did, text = item
            try:
                raw = self.document_parser.search_related_content(
                    text=text,
                    goal=query,
                    max_length=max_length,
                )
                related = self._parse_related_content(raw)
                return did, related
            except Exception:
                return did, ""

        pairs = list(id2text.items())
        workers = min(self.max_workers, len(pairs))
        outputs: Dict[str, str] = {}
        with ThreadPoolExecutor(max_workers=max(1, workers)) as ex:
            futures = [ex.submit(_work, pair) for pair in pairs]
            for f in as_completed(futures):
                did, related = f.result()
                if related:
                    outputs[did] = related

        if not outputs:
            return "（无）"

        ordered_outputs = []
        for did in document_ids:
            txt = outputs.get(did)
            if txt:
                ordered_outputs.append((did, txt))
        lines = [f"{did}: {txt}" for did, txt in ordered_outputs]
        return "\n".join(lines)

@register_tool("bm25_search_docs")
class BM25SearchDocsTool(BaseTool):
    """
    基于 LangChain BM25 的稀疏检索（可选 rerank）
    - 初始化：documents(List[Document])、reranker(Optional)
    - 调用：params={"query": str, "k": int?}
    - 输出：返回检索命中的正文与 metadata，避免额外包裹抬头
    """

    name = "bm25_search_docs"
    description = "基于关键词和词面重合的 BM25 文本检索工具。适合查找专有名词、缩写全称、术语原文、固定表述，以及问题中出现过明确字词的证据段落。"
    parameters = [
        {"name": "query", "type": "string", "description": "检索文本，适合直接输入问题中的关键词、缩写、专有名词或想确认的原句片段", "required": True},
        {"name": "k", "type": "integer", "description": "返回条数（默认 10）", "required": False},
    ]

    def __init__(
        self,
        documents: List[Document],
        *,
        use_zh_preprocess: bool = True,
        k_default: int = 10,
    ):
        self.retriever = KeywordBM25Retriever(
            documents=documents,
            zh_preprocess=(zh_preprocess if use_zh_preprocess else None),
            k_default=k_default
        )

    def call(self, params: str, **kwargs) -> str:
        logger.info("🔎 调用 bm25_search_docs")
        try:
            p: Dict[str, Any] = json.loads(params or "{}")
        except Exception as e:
            return f"参数解析失败：{e}"

        query = str(p.get("query", "")).strip()
        if not query:
            return "query 不能为空"
        k = int(p.get("k", 10))
        expanded_query = _expand_bm25_query(query)

        try:
            hits = self.retriever.retrieve(query=expanded_query, k=k)
        except Exception as e:
            logger.exception("BM25 检索失败")
            return f"检索失败：{e}"

        if not hits:
            return "No results."

        return _format_hits_mandatory(hits)
